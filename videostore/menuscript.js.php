//*** SUB MENU FUNCTIONS ***//
// GET ELEMENT ID MULTI BROWSER
function getElement_menuhdr(id) {
	return document.getElementById ? document.getElementById(id) : document.all ? document.all(id) : null; 
}

// GET X COORDINATE
function getRealLeft_menuhdr(id) { 
	var el = getElement_menuhdr(id);
	if (el) { 
		xPos = el.offsetLeft;
		tempEl = el.offsetParent;
		while (tempEl != null) {
			xPos += tempEl.offsetLeft;
			tempEl = tempEl.offsetParent;
		} 
		return xPos;
	} 
} 

// GET Y COORDINATE
function getRealTop_menuhdr(id) {
	var el = getElement_menuhdr(id);
	if (el) { 
		yPos = el.offsetTop;
		tempEl = el.offsetParent;
		while (tempEl != null) {
			yPos += tempEl.offsetTop;
			tempEl = tempEl.offsetParent;
		}
		return yPos;
	}
}

// MOVE OBJECT TO COORDINATE
function moveObjectTo_menuhdr(objectID,x,y) {
	var el = getElement_menuhdr(objectID);
	el.style.left = x;
	el.style.top = y;
}

// MOVE SUBMENU TO CORRESPONDING BUTTON
function showSubMenu_menuhdr(subID, buttonID) {
	hideAllSubMenus_menuhdr();
	butX = getRealLeft_menuhdr(buttonID);
	butY = getRealTop_menuhdr(buttonID);
	moveObjectTo_menuhdr(subID,butX+xSubOffset_menuhdr, butY+ySubOffset_menuhdr);
}

// HIDE ALL SUB MENUS
function hideAllSubMenus_menuhdr() {
	for ( x=0; x<totalButtons_menuhdr; x++) {
		moveObjectTo_menuhdr("submenu" + (x+1) + "_menuhdr",-500, -500 );
	}
}

// HIDE ONE SUB MENU
function hideSubMenu_menuhdr(subID) {
	if ( overSub_menuhdr == false ) {
		moveObjectTo_menuhdr(subID,-500, -500);
	}
}



